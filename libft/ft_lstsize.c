#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int	i;

	i = 0;
	if (!lst)
		return (0);
	while (lst->next != T_NULL)
	{
		lst = lst->next;
		i++;
	}
	i++;
	return (i);
}
