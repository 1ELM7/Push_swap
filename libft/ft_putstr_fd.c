#include "libft.h"
#include <unistd.h>

void	ft_putstr_fd(char *str, int fd)
{
	int	a;

	if (!str)
		return ;
	a = 0;
	while (str[a] != '\0')
	{
		write (fd, &str[a], 1);
		a++;
	}
}
