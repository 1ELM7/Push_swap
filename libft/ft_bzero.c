#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*ends;

	i = 0;
	ends = (unsigned char *)s;
	while (i < n)
	{
		ends[i] = '\0';
		i++;
	}
}
